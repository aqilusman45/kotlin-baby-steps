fun main(){
    // Priorities while performing mathematical operations
    // Just like in maths Kotlin follows BODMAS rule
    // which is as follows
    // 1- ()
    // 2- ^
    // 3- *, /
    // 4- +, -
    // 5- =
    var n1 = 20;
    var n2 =30;
    var n3 = 4;
    var n4 = 3;
    var exp: Int?;
    exp = n1+n4 * n3 - n2;
    println(exp);
}